package com.helldefender.radardemo;

public class RaDar {

    private double start;//起始点

    private double end;//终止点

    public RaDar() {
        start = 0.0;
        end = 0.0;
    }

    public double getStart() {
        return start;
    }

    public void setStart(double start) {
        this.start = start;
    }

    public double getEnd() {
        return end;
    }

    public void setEnd(double end) {
        this.end = end;
    }
}
